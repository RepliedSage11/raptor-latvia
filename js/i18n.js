$('.langChange li').click(function (e) {
	const el = $(this);
	if (el.attr('id') !== language) {
		$('.langChange li.active').removeClass('active');
		el.addClass('active');
		language = this.id;
		$.i18n().locale = this.id;
		window.localStorage.setItem('lang', this.id);
		$('body').i18n();
		
		if ($('#pdf').length) {
			PDFObject.embed(`pdf/RAPTOR_${language}.pdf`, "#pdf");
		}

	}
});

